void main(){
  //Arithemetics: +,-,*,/,%
  print("Example of Arithemetic Operators");
  var a = 10;
  var b = 5;
  print(a+b);
  print("a + b = ${a+b}");
  print("a - b = ${a-b}");
  print("a * b = ${a*b}");
  print("a / b ${a/b}");
  print("a % b = ${a%b}");
  print("a~/b = ${a~/b}");
  print("-(a-b) = ${-(a-b)}");//Unary minus
}